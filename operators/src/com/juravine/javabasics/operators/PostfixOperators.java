package com.juravine.javabasics.operators;

public class PostfixOperators {

    public static void main(String[] args) {
        //Postfix operators

        int c = 1;

        //increment
        c++;
        System.out.println("c after increment " + c);

        //decrement
        c--;
        System.out.println("c after decrement " + c);
    }
}
