package com.juravine.javabasics.operators;

public class UnaryOperators {

    public static void main(String[] args) {

        //Unary operators
        int d = 4;

        //increment
        ++d;
        System.out.println("d after increment " + d);

        //decrement
        --d;
        System.out.println("d after decrement " + d);

    }
}
