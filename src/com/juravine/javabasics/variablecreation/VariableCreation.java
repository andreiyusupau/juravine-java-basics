package com.juravine.javabasics.variablecreation;

public class VariableCreation {

    public static void main(String... args) {
        //variable declaration
        int a;

        //multiple variables declaration
        int b,c,d;

        //separate variable declaration and initialization
        int x;
        x = 5;
        System.out.println(x);

        //combined variable declaration and initialization
        int y = 5;
        System.out.println(y);

        //multiple variable declaration and initialization
        int r, s, t = 5;
        System.out.println(t);
    }
}
